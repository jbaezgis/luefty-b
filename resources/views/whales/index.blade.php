        <p class="lead">Humpbacks feed almost exclusively in the summer months when they can be found in the northernmost and southernmost cold arctic waters, rich in krill, plankton and small fish. Krill, tiny crustaceans found in abundance in the world’s cold waters, and plankton are filtered through an array of balene plates found in the whale’s upper mouth. Humpbacks are ‘Balene’ whales, which feed by taking in huge gulps of sea water and pushing the water back out through the balene filter system in a process called ‘filter feeding’. The krill and other food is trapped by the filtration system consisting of bristles between the plates. Balene was once more commonly called ‘whalebone’, famously used in corset stays and petticoats when these were fashionable. Humpback whales rarely feed at all during the winter months, generally surviving on their fat reserves during the time they can be found in the Bay of Samana.</p>
